public class Gravel {

    private String gravel;
    private Color color[];

    /**
     * @return the gravel
     */
    public String getGravel() {
        return gravel;
    }

    /**
     * @param gravel the gravel to set
     */
    public void setGravel(String gravel) {
        this.gravel = gravel;
    }

    /**
     * @return the color
     */
    public Color[] getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(Color[] color) {
        this.color = color;
    }

}
