package aquarium;

/**
 *
 * @author Douglas
 */
public class Color {

    private String color;
    private String ypr;

    /**
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

    /**
     * @return the ypr
     */
    public String getYpr() {
        return ypr;
    }

    /**
     * @param ypr the ypr to set
     */
    public void setYpr(String ypr) {
        this.ypr = ypr;
    }
}
